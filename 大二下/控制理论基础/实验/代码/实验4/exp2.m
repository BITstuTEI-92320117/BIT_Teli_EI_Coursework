% 定义第一个传递函数（比例+微分环节）  
G1 = tf([100, 400], 1);    
  
% 定义第二个传递函数（积分环节）  
G2 = tf(1, [1, 0]);       
  
% 定义第三个传递函数（一阶惯性环节）    
G3 = tf(1, [1, 0.5]);     
  
% 定义第四个传递函数（二阶振荡环节）  
G4 = tf(1, [1, 100, 2500]);    
 
% 计算串联系统的开环传递函数（各环节相乘）  
G = G1 * G2 * G3 * G4;     
  
% 绘制Bode图并显示幅值裕度和相位裕度  
margin(G)    
grid on
