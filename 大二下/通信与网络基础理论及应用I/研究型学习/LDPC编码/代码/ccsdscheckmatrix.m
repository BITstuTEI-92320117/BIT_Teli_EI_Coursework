function [ H ] = ccsdscheckmatrix( M, RATE )
% 根据 CCSDS 标准（2006版）生成 LDPC 码的校验矩阵 H
% 输入参数：
%   M     : CCSDS 标准中定义的矩阵参数，决定矩阵维度
%   RATE  : 编码速率（支持 1/2, 2/3, 4/5）
% 输出：
%   H     : 生成的 LDPC 校验矩阵

%% 预定义参数（来自 CCSDS 标准文档）
% theta: 循环移位参数序列，共26个元素，用于构建子矩阵 C
theta=[1 1 2 3 1 1 2 3 1 2 3 0 2 3 0 2 3 0 1 3 0 1 3 0 1 2];

% fai: 位移参数矩阵，4行26列，用于计算子矩阵 C 的非零元素位置
% 每列对应一个子矩阵 C_k 的位移值，行索引由 f_4i_M 决定
fai=[1787	1077	1753	697	1523	5	2035	331	1920	130	4	85	551	15	1780	1960	3	145	1019	691	132	42	393	502	201	1064
1502	602	749	1662	1371	9	131	1884	1268	1784	19	1839	81	2031	76	336	529	74	68	186	905	1751	1516	1285	1597	1712
1887	521	590	1775	1738	2032	2047	85	1572	78	26	298	1177	1950	1806	128	1855	129	269	1614	1467	1533	925	1886	2046	1167
1291	301	1353	1405	997	2032	11	1995	623	73	1839	2003	2019	1841	167	1087	2032	388	1385	885	707	1272	7	1534	1965	588];

%% 初始化基础矩阵块
A = zeros(M);       % M×M 的零矩阵，用于结构占位
B = eye(M);         % M×M 的单位矩阵，表示对角线连接

%% 构建循环移位子矩阵 C_k（共26个）
L = 0:M-1;          % 行/列索引向量 [0,1,...,M-1]
C = cell(1,26);     % 预分配单元格数组存储26个子矩阵

for matrixNum = 1:26
    % 获取当前子矩阵参数
    t_k = theta(matrixNum);               % 从 theta 中取循环移位值
    f_4i_M = floor(4 * L / M);            % 计算分组索引（0~3），用于选择 fai 的行
    f_k = fai(f_4i_M + 1, matrixNum)';    % 从 fai 矩阵取对应行的位移参数（转置为列向量）

    % 计算非零元素的位置（列索引）
    col_1 = M/4 * (mod(t_k + f_4i_M, 4)) + ...  % 大分组偏移（每块大小为 M/4）
        mod(f_k + L, M/4);                % 组内偏移

    % 将线性索引转换为行列下标（MATLAB 按列存储）
    row_col = col_1 + 1 + L * M;          % +1 因 MATLAB 索引从1开始
    C_temp = zeros(M);                    % 初始化临时矩阵
    C_temp(ind2sub([M,M], row_col)) = 1;  % 在计算位置置1

    % 存储转置后的矩阵（适配 CCSDS 结构要求）
    C{matrixNum} = C_temp';
end

%% 构建基础校验矩阵 H（码率1/2时的结构）
% H 由水平拼接的块矩阵组成，包含 A、B 和 C_k 的组合
H = [A A A B B+C{1};B+C{8} B+C{7}+C{6} A A B;A B B+C{5} A C{4}+C{3}+C{2}];

%% 根据码率扩展校验矩阵
switch(RATE)
    case 1/2
        % 码率1/2直接使用基础矩阵
        H=H;
        gridCol = 1:4;  % 网格列标记（用于可视化分块）

    case 2/3
        % 扩展为码率2/3的结构
        H_23 = [A A;B C{11}+C{10}+C{9};C{14}+C{13}+C{12} B];
        H = [H_23 H];   % 水平拼接原有矩阵
        gridCol = 1:6;  % 分块列标记

    case 4/5
        % 扩展为码率4/5的结构
        H_23 = [A A;B C{11}+C{10}+C{9};C{14}+C{13}+C{12} B];
        H_45 = [A A A A;B C{23}+C{22}+C{21} B C{17}+C{16}+C{15};...
            C{26}+C{25}+C{24} B C{20}+C{19}+C{18} B];
        H = [H_45 H_23 H];  % 三级拼接
        gridCol = 1:10;     % 分块列标记
end

%% 可视化校验矩阵结构
figure;
spy(H);  % 绘制 H 的非零元素分布
hold on;

% 添加红色分块线（辅助观察矩阵结构）
gridH = zeros(size(H));
gridH([M, 2*M], :) = 1;        % 水平分块线（每 M 行分割）
gridH(:, M*gridCol) = 1;        % 垂直分块线（每 M 列分割）
spy(gridH, 'r', 4);             % 红色显示分块线
title('Check Matrix Structure (CCSDS Standard)');
hold off;

end