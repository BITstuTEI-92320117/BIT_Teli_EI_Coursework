% 定义系统传递函数的分子和分母多项式系数   
num = [2, 18, 40];    % 分子多项式系数: 2s^2 + 18s + 40   
den = [1, 5, 8, 6];   % 分母多项式系数: s^3 + 5s^2 + 8s + 6   
    
Gtf = tf(num, den)    % 创建传递函数对象   

Gzpk = zpk(Gtf)       % 转换为零极点增益形式   

Gss = ss(Gtf)         % 转换为状态空间形式   

% 绘制零极点图   
figure;                % 创建新图形窗口   
pzmap(Gzpk);           % 绘制零极点图   
title('系统零极点图');  % 添加标题   
xlabel('实轴');        % x轴标签   
ylabel('虚轴');        % y轴标签   
grid on;               % 显示网格   