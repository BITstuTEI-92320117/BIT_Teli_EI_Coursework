% 1. 定义各子系统的传递函数分母  
den1 = [1, 1];    
den2 = [0.5, 1];   
den3 = [1, 0];     
	  
% 2. 创建各子系统传递函数  
G1 = tf(1, den1);   
G2 = tf(1, den2);   
H = tf(1, den2);    
G3 = tf(3, den3);  
  
% 3. 构建前向通道传递函数  
G0 = (G1 + G2) * G3;      
disp('前向通道传递函数G0:');  
disp(G0);  
  
% 4. 构建闭环系统传递函数  
G = feedback(G0, H)   
